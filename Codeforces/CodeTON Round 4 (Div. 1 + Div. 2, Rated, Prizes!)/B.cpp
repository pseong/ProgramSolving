void solve(int CASE) {
    int n;
    cin >> n;
    if (n%2 == 0) {
        cout << -1 << '\n';
        return;
    }
    vector<int> ans;
    while (n != 1) {
        n /= 2;
        if (n%2) ans.push_back(2);
        else {
            n++;
            ans.push_back(1);
        }
    }
    cout << ans.size() << '\n';
    for (int i=(int)ans.size()-1; i>=0; i--) {
        cout << ans[i] << ' ';
    }
    cout << '\n';
}

