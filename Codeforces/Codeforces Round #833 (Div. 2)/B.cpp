void solve(int CASE) {
    int n;
    string s;
    cin >> n >> s;
    ll ans = 0;
    for (int l=0; l<n; l++) {
        vector<int> chk(10);
        int cnt = 0;
        int mx = 0;
        for (int r=l; r<min(n, l+100); r++) {
            if (chk[s[r]-'0']++ == 0) cnt++;
            mx = max(mx, chk[s[r]-'0']);
            if (mx <= cnt) ans++;
        }
    }
    cout << ans << '\n';
}

