void no() { cout << "No" << '\n'; }
void yes() { cout << "Yes" << '\n'; }

void solve() {
    int n; cin >> n;
    vector<string> v(2);
    cin >> v[0] >> v[1];
    for (int i=0; i<n; i++) {
        if (v[0][i] == '1' && v[1][i] == '1') {
            no();
            return;
        }
    }
    yes();
}

signed main() {
    ios::sync_with_stdio(false);
    cin.tie(0);

    int T; cin >> T;
    while (T--) {
        solve();
    }
}